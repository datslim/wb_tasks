	fmt.Printf("–í—Ö–æ–¥–Ω–∞—è —Å—Ç—Ä–æ–∫–∞: %s, –ø–µ—Ä–µ–≤–µ—Ä–Ω—É—Ç–∞—è —Å—Ç—Ä–æ–∫–∞: %s\n", englishString, reverseString(englishString))
	fmt.Printf("–í—Ö–æ–¥–Ω–∞—è —Å—Ç—Ä–æ–∫–∞: %s, –ø–µ—Ä–µ–≤–µ—Ä–Ω—É—Ç–∞—è —Å—Ç—Ä–æ–∫–∞: %s\n", cyrillicString, reverseString(cyrillicString))
