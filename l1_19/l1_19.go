// —Ñ—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–µ—Ä–µ–≤–æ—Ä–æ—Ç–∞ —Å—Ç—Ä–æ–∫–∏
func reverseString(inputString string) string {
	// –¥–ª—è –ø—Ä–∞–≤–∏–ª—å–Ω–æ–π –æ–±—Ä–∞–±–æ—Ç–∫–∏ —Å–∏–º–≤–æ–ª–æ–≤, —Ä–∞–∑–º–µ—Ä –∫–æ—Ç–æ—Ä—ã—Ö –Ω–µ —Ä–∞–≤–µ–Ω 1 –±–∞–π—Ç—É –∏ –Ω–∞–º –Ω–µ –∏–∑–≤–µ—Å—Ç–µ–Ω, –ø—Ä–∏–≤–µ–¥–µ–º –≤—Ö–æ–¥–Ω—É—é —Å—Ç—Ä–æ–∫—É
	// –∫ —Ç–∏–ø—É []rune –∏ –±—É–¥–µ–º –º–µ–Ω—è—Ç—å –ø–æ—Ä—è–¥–æ–∫ —Å–∏–º–≤–æ–ª–æ–≤ in-place
	stringRunes := []rune(inputString)

	// –∏—Å–ø–æ–ª—å–∑—É–µ–º –¥–≤–∞ —É–∫–∞–∑–∞—Ç–µ–ª—è, –æ–¥–∏–Ω –∏–∑ –∫–æ—Ç–æ—Ä—ã—Ö —É–∫–∞–∑—ã–≤–∞–µ—Ç –Ω–∞ –Ω–∞—á–∞–ª–æ, –∞ –≤—Ç–æ—Ä–æ–π –Ω–∞ –∫–æ–Ω–µ—Ü –∏—Å—Ö–æ–¥–Ω–æ–≥–æ —Å—Ä–µ–∑–∞
	// —Ç–∞–∫–∏–º –æ–±—Ä–∞–∑–æ–º –∑–∞ –æ–¥–∏–Ω –ø—Ä–æ—Ö–æ–¥, –º—ã –º–æ–∂–µ–º –ø–µ—Ä–µ–ø–∏—Å–∞—Ç—å –Ω–∞—à —Å–ª–∞–π—Å –Ω–∞ –æ–±—Ä–∞—Ç–Ω—ã–π –ø–æ—Ä—è–¥–æ–∫ —Å–∏–º–≤–æ–ª–æ–≤
	l := 0
	r := len(stringRunes) - 1

	for l <= r { // –ø–æ–∫–∞ —É–∫–∞–∑–∞—Ç–µ–ª–∏ –Ω–µ –≤—Å—Ç—Ä–µ—Ç–∏–ª–∏—Å—å
		stringRunes[l], stringRunes[r] = stringRunes[r], stringRunes[l] // –º–µ–Ω—è–µ–º –Ω–∞—á–∞–ª–æ —Å –∫–æ–Ω—Ü–æ–º
		l++                                                             // –ª–µ–≤—ã–π —É–∫–∞–∑–∞—Ç–µ–ª—å –¥–≤–∏–≥–∞–µ–º –≤–ø–µ—Ä–µ–¥
		r--                                                             // –ø—Ä–∞–≤—ã–π —É–∫–∞–∑–∞—Ç–µ–ª—å –¥–≤–∏–≥–∞–µ–º –Ω–∞–∑–∞–¥
	}
	return string(stringRunes) // –ø—Ä–∏–≤–æ–¥–∏–º –Ω–∞—à —Å—Ä–µ–∑ –∫ —Å—Ç—Ä–æ–∫–µ
}

